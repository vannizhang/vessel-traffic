export const getLayerDataByDate = (date:Date):ShipTrafficLayerInfo=>{

    if(!date){
      return null
    }
  
    const year = date.getFullYear();
    const month = date.getMonth() + 1;

    const layerInfo = ShipTrafficLayersData.filter(d=>{
        return d.Year === year && d.Month === month;
    })[0];

    return layerInfo;
}